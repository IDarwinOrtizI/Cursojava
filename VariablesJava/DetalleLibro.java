package VariablesJava;

public class DetalleLibro {
    public static void main(String[] args) {
        // Detalle del libro
        String tituloLibro = "El principito";
        int añoPublicacion = 1954;
        boolean libroDisponible = true;
        double precio = 500.50;

        // Imprimir el valor de las variables
        System.out.println("Nombre Del Libro: " + tituloLibro);
        System.out.println("Año de Publicación: " + añoPublicacion);
        System.out.println("¿Está disponible? " + libroDisponible);
        System.out.println("Precio: $" + precio);
    }
}
